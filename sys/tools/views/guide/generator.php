<h1>The Generator</h1>

<p>Here's what the Generator does for you:</p>
<dl>
	<dt>Add Models and Controllers</dt>
		<dd>Using template Models and Controllers it creates the files and folders necessary for the next part of your project.</dd>
	</dd>
	<dt>Add Modules</dt>
		<dd>You can add a module, which will add the controller, the default views, append state information to your state config, append validation functions and add migrations to your project. You can also tell the script generator to go ahead and apply the migrations when you add the module.</dd>
	<dt>Privileges</dt>
		<dd>Handle the creation of privileges and their assignment to roles for the project.</dd>
	<dt>Schema</dt>
		<dd>Create form elements from a schema, build static model functions, and create your database.</dd>
	<dt>Tests</dt>
		<dd>Run unit tests in the style of J-Unit</dd>
	<dt>Deploy the project</dt>
		<dd>coming soon</dd>
</dl>